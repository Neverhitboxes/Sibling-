public class Sibling
{
  private String name;
  private int age;
  private int weight;
  private int height;

  public Sibling (String n, int a, int w, int h)
  {
    name = n;
    age = a;
    weight = w;
    height = h;
  }
  public String getName ( )
  {
    return name;
  }
  public int getAge ( )
  {
    return age;
  }
  public int getWeight ( )
  {
    return weight;
  }
  public int getHeight ( )
  {
	return height;
}
}

